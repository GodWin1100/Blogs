def add(num1: float, num2: float):
    """Add two numbers `num1` and `num2`

    Args:
        num1 (float): First Number
        num2 (float): Second Number
    """
    print(f"{num1} + {num2} = {num1 + num2}")
    return num1 + num2


def subtract(num1: float, num2: float):
    """Subtract two numbers `num1` and `num2`

    Args:
        num1 (float): First Number
        num2 (float): Second Number
    """
    print(f"{num1} - {num2} = {num1 - num2}")
    return num1 - num2


def multiply(num1: float, num2: float):
    """Multiply two numbers `num1` and `num2`

    Args:
        num1 (float): First Number
        num2 (float): Second Number
    """
    print(f"{num1} * {num2} = {num1 * num2}")
    return num1 * num2


def divide(num1: float, num2: float):
    """Divide two numbers `num1` and `num2`

    Args:
        num1 (float): First Number
        num2 (float): Second Number
    """
    print(f"{num1} / {num2} = {num1 / num2}")
    return num1 / num2
